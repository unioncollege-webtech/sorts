function quickSort(origArray)
{
    var array = origArray;
    var pivot = array.pop;
    var smaller = [], larger = [], middle =[pivot];
    
    for(var i=0; i<array.length;i++)
    {
        var test=array.shift;
        
        if(test===pivot)
        {
            middle.push(test);
        }
        else if(test>pivot)
        {
            larger.push(test);
        }
        else  //lesser elements
        {
            smaller.push(test);
        }
    }
    
    
}